Eₚ = 12
Eg = 7
Δ = 3

VB0 = 0
CS0 = 5
CH0 = 7
CL0 = 8

γᴸ = [2, 3, 4]

γ = γᴸ .- 1 ./ [3, 6, 6] * Eₚ / Eg




#Eₚ = Eₚ_publikacja / 2 
P = √(Eₚ * ħ^2 / (2m₀))

P₊ = P * (k[1] + im * k[2])
P₋ = P * (k[1] - im * k[2])
P_z = P * k[3]

γᵥ = 1 / mₕ - Eₚ / 3 * (2 / Eg + 1 / (Eg + Δ))

VB = VB0 - ħ^2 / (2m₀) * γᵥ * (k[1]^2 + k[2]^2 + k[3]^2)
CS = CS0 + ħ^2 / (2m₀) * γ[1] * (k[1]^2 + k[2]^2 + k[3]^2)
CH = CH0 + ħ^2 / (2m₀) * ((γ[1] + γ[2]) * (k[1]^2 + k[2]^2) + (γ[1] - 2γ[2]) * k[3]^2)
CL = CL0 + ħ^2 / (2m₀) * ((γ[1] - γ[2]) * (k[1]^2 + k[2]^2) + (γ[1] + 2γ[2]) * k[3]^2)

S = ħ^2 / (2m₀) * 2√3 * γ[3] * (-k[1] + im * k[2]) * k[3]
R = ħ^2 / (2m₀) * √3 * (γ[2] * (k[1]^2 - k[2]^2) - 2im * γ[3] * k[1] * k[2])
D = ħ^2 / (2m₀) * √2 * γ[2] * (k[1]^2 + k[2]^2 - 2k[3]^2)






Ĥ = [
