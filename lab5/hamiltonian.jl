    1/√2*P₋ 0 CH S -R 0 S/√2 -√2*R
    -√2/√3*P_z 1/√6*P₋ S' CL 0 -R -D -√3/√2*S
    -1/√6*P₊ -√2/√3*P_z -R' 0 CL -S -√3/√2*S' D
    0 -1/√2*P₊ 0 -R' -S' CH √2*R' 1/√2*S'
    -1/√3*P_z -1/√3*P₋ 1/√2*S' -D -√3/√2*S √2*R CS 0
    -1/√3*P₊ 1/√3*P_z -√2*R' -√3/√2*S' D 1/√2*S 0 CS
]
